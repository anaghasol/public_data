  <script>
    $(document).ready(function () {
    let iti;

    function openModal() {
        $('#contactModal').addClass('show');
        $('body').addClass('modal-open phone-modal-active');

        setTimeout(() => {
            const phoneInput = document.querySelector("#contactPhone");
            if (!phoneInput) return;

            if (iti) iti.destroy();

            iti = window.intlTelInput(phoneInput, {
                initialCountry: "us",
                separateDialCode: true,
                nationalMode: false,
                allowDropdown: true,
                //  dropdownContainer: document.body,
                utilsScript: "https://cdn.jsdelivr.net/npm/intl-tel-input@17.0.19/build/js/utils.js"
            });
        }, 300);
    }
function initPhoneInput(input) {
  const isMobile = window.innerWidth <= 768;

  const options = {
    separateDialCode: true,
    initialCountry: "in"
  };

  if (isMobile) {
    options.dropdownContainer = document.body;
  }

  return window.intlTelInput(input, options);
}

    function closeModal() {
        $('#contactModal').removeClass('show');
        $('body').removeClass('modal-open phone-modal-active');

        if (iti) {
            iti.destroy();
            iti = null;
        }
    }

    $(document).on('click', '.contactModalBtn', function(e){
        e.preventDefault();
        openModal();
    });

    $(document).on('click', '.close_modal', function(){
        closeModal();
    });

    $(document).on('click', '#contactModal', function(e){
        if ($(e.target).is('#contactModal')) closeModal();
    });



    /* ================= CONTACT FORM ================= */
    const contactForm = document.querySelector("#contactForm");
    const messageBox = document.getElementById("formSuccess");

    if (contactForm && messageBox) {

        contactForm.addEventListener("submit", function (e) {
            e.preventDefault();

            const formData = new FormData(contactForm);
            const submitBtn = contactForm.querySelector("button[type='submit']");

            submitBtn.disabled = true;
            submitBtn.textContent = "Sending...";

            fetch("api/contact-process.php", {
                method: "POST",
                body: formData
            })
            .then(res => res.json())
            .then(response => {

                messageBox.style.display = "block";
                messageBox.classList.remove("success", "error");

                if (response.status === "success") {

                    messageBox.classList.add("success");
                    messageBox.innerHTML = "<b>Request submitted successfully!</b>";

                    contactForm.reset();

                    setTimeout(() => {
                        closeModal();
                        messageBox.style.display = "none";
                        messageBox.classList.remove("success");
                    }, 2000);

                } else {
                    messageBox.classList.add("error");
                    messageBox.innerHTML =
                        "<b>Sorry! Your request could not be sent.</b>";
                }

                submitBtn.disabled = false;
                submitBtn.textContent = "Submit";

            })
            .catch(() => {

                messageBox.style.display = "block";
                messageBox.classList.add("error");
                messageBox.innerHTML =
                    "<b>Server error. Please try again later.</b>";

                submitBtn.disabled = false;
                submitBtn.textContent = "Submit";
            });

        });
    }

    /* ================= TECHNOLOGY SELECT ================= */
    const techSelect = document.querySelector('select[name="technology"]');

    if (techSelect) {
        techSelect.addEventListener("change", function () {
            if (this.value !== "Other") return;

            const input = document.createElement("input");
            input.type = "text";
            input.name = this.name;
            input.className = this.className;
            input.placeholder = "Enter your technology";
            input.required = true;

            this.replaceWith(input);
            input.focus();
        });
    }

    /* ================= CLIENTS FETCH ================= */
    fetch("api/clients.php")
        .then(res => res.json())
        .then(data => {

            const container = document.getElementById("clientsContainer");
            if (!container) return;

            container.innerHTML = "";

            data.forEach(item => {
                const div = document.createElement("div");
                div.className = "oc_img";

                const img = document.createElement("img");
                img.src = item.image;
                img.alt = "Client Logo";

                div.appendChild(img);
                container.appendChild(div);
            });

            if ($('.ourCients').hasClass('slick-initialized')) {
                $('.ourCients').slick('unslick');
            }

            $('.ourCients').slick({
                slidesToShow: 6,
                slidesToScroll: 1,
                autoplay: true,
                autoplaySpeed: 2500,
                speed: 800,
                cssEase: 'ease-in-out',
                infinite: true,
                arrows: false,
                dots: false,
                pauseOnHover: true,
                responsive: [
                    { breakpoint: 1200, settings: { slidesToShow: 5 }},
                    { breakpoint: 992, settings: { slidesToShow: 4 }},
                    { breakpoint: 768, settings: { slidesToShow: 3 }},
                    { breakpoint: 576, settings: { slidesToShow: 2 }},
                    { breakpoint: 400, settings: { slidesToShow: 1 }}
                ]
            });

        });

});

