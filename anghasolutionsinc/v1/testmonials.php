const testimonialForm = document.getElementById("testimonialForm");
const messageBox = document.getElementById("formSuccess");

if (testimonialForm) {

  testimonialForm.addEventListener("submit", function (e) {
    e.preventDefault();

    const formData = new FormData(testimonialForm);

    fetch("api/testimonials-sendmail.php", {
      method: "POST",
      body: formData
    })
    .then(response => response.json())
   .then(data => {

  if (data.status === "success") {

    messageBox.innerHTML = "Thank you! Your testimonial has been submitted.";
    messageBox.classList.add("success");
    messageBox.style.display = "block";

    testimonialForm.reset();

    // 🔥 Close modal after 2 seconds
    setTimeout(() => {
      document.getElementById("testmonials_modal").style.display = "none";
    }, 2000);

  } else {

    messageBox.innerHTML = data.message;
    messageBox.classList.add("error");
    messageBox.style.display = "block";

  }

    })
    .catch(error => {

      messageBox.innerHTML = "Something went wrong. Please try again.";
      messageBox.classList.add("error");
      messageBox.style.display = "block";

    });

  });

}

